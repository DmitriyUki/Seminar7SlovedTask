//задаем параметры (rows и columns) веденные пользователем как целые числа. При этом переменная double по умолчанию использует границы
// рандомных чисед и поэтому дополнительно определять параметры границ матрицы безсмысленно.
{
    double [, ] matrix = new double[rows, columns]; // создаем двумерную матриц вещественных чисел, используя переменную  double
    // под названием matrix, выделяем под него память,  используя параметры введеные пользователем.

    for(int i = 0; i < matrix.GetLength(0); i++) // цикл который все массивы(строки), под вторым условием можно написать и rows, 
