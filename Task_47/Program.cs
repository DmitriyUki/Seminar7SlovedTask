    //от матрицы колличество строк GetLength(0). определяем i как переменную int потому-что числа заданные пользователем int
    {
        for(int j = 0; j < matrix.GetLength(1); j++) // создаем внутренний(вложенный) цикл который будет перебирать элементы 
      //внутри стоки(столбцы) GetLength(1) выдаст нам именно колличество столбцов, тут надо просто запомнить что 0 для строк, 1 для столюцов
      //тоже самое самое j 
      {
        matrix [i, j] = Random.Shared.NextDouble() * 10 -5; // обращаемся к нашему элементу по i и j создаем рандомные значения, задав диапазон
      }
    }

  return matrix;

}

